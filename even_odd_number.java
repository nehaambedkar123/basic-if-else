public class even_odd_number {
 
    public static void main(String[]args) {
       byte var1 = 37;
       if (var1 % 2 == 0) {
          System.out.println("" + var1 + "is an even number");
       } else {
          System.out.println("" + var1 + "is an odd number");
       }
 
    }
 }

